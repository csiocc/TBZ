package TBZ.m319.Java.KN03.quiz;

public class BiggerThen {
  public static void main(String[] args) {
    int x = 10;
    int y = 5;

    if (x == y) {
      System.out.println("Even");
    } 
    else if (x > y) {
      System.out.println("x is bigger");
    } 
    else if (x < y) {
      System.out.println("y is bigger");
    }
  }
}
